    % Landscape size
    L = max(60, Tower.Diameter(1) + 5);
    W = max(L, Blade.Radius(end) + 1);
    
    % Patch of grass
    [x,y] = ndgrid(...
        linspace(-L/2, L/2, 10), ...
        linspace(-L/2, L/2, 10));
