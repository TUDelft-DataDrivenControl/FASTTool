    faces = [1 2 6 5; 2 3 7 6; 3 4 8 7; 4 1 5 8; 1 2 3 4; 5 6 7 8];
    patch('Vertices', vert', 'Faces', faces, ...
        'FaceColor', handles.Color.Brown, ...
        'EdgeColor', handles.Color.EdgeColor, ...
